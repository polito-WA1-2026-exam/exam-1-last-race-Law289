function getAllConnections() {
    const sql = `
        SELECT 
            connections.*,
            stationA.name as stationA_name,
            stationB.name as stationB_name,
            lines.name as line_name,
            lines.color as line_color
        FROM connections
        JOIN stations as stationA ON connections.stationA_ID = stationA.id
        JOIN stations as stationB ON connections.stationB_ID = stationB.id
        JOIN lines ON connections.lineID = lines.id
    `;
    return new Promise((resolve, reject) => {
        db.all(sql, (err, rows) => {
            if (err) {
                console.error(err);
                reject(err);
            } else {
                resolve(rows.map(row => new Connection({
                    stationA: new Station({id: row.stationA_ID, name: row.stationA_name}), 
                    stationB: new Station({id: row.stationB_ID, name: row.stationB_name}), 
                    line: {id: row.lineID, name: row.line_name, color: row.line_color}
                })));
            }
        })
    })
}

