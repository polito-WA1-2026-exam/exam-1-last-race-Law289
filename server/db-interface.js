function getAllLines() {
    return new Promise((resolve, reject) => {
        const sql = 'SELECT * FROM lines';
        db.all(sql, async (err, rows) => {
            if (err) {
                console.log(err);
                return reject(err);
            }
            const lines = await Promise.all(rows.map(async row => {
                const stops = await getLineStops(row.id);
                return new Line({
                    id: row.id,
                    name: row.name,
                    color: row.color,
                    stations: stops
                });
            }));
            resolve(lines);
        })
    })
}

function getLine(lineID) {
    return new Promise((resolve, reject) => {
        const sql = 'SELECT * FROM lines WHERE id = ?';
        db.get(sql, [lineID], async (err, row) => {
            if (err) {
                console.error(err);
                return reject(err);
            }
            if (!row) {
                return reject(new Error(`Line with id ${lineID} not found`));
            }
            const stops = await getLineStops(lineID);
            resolve(new Line({
                id: lineID,
                name: row.name,
                color: row.color,
                stations: stops
            }));
        })
    })
}

/**
 * Adds the given line to the database, populating also the stations, connections and line_stations tables.
 * All stations crossed by the line are added to the stations table if not already present, and 
 * consecutive stations are added to the connections table. Also, the table containing the line stops gets filled.
 * @param {Line} line - The line object to add to database. ID and stop list can be undefined,
 * but at least name and color must be defined. 
 * @returns A promise that resolves in the line ID in the database. If the line was already present in the table, the old one
 * is returned, otherwise a new one is generated by autoincrement.
 */
function addLine(line) {
    return new Promise((resolve, reject) => {
        const lineSQL = 'INSERT OR IGNORE INTO lines(name, color) VALUES(?, ?)';
        const stopsSQL = 'INSERT OR IGNORE INTO line_stations(lineID, stationID, stopOrder) VALUES(?, ?, ?)';
        const connectionSQL = 'INSERT OR IGNORE INTO connections(stationA_ID, stationB_ID, lineID) VALUES(?, ?, ?)';
        db.run(lineSQL, [line.name, line.color], function(err) {
            if (err) {
                console.error(err);
                return reject(err);
            }
            line.id = this.lastID;
            const lineID = this.lastID;

            if (!line.stations || line.stations.length === 0)
                return resolve(lineID);

            const ensurePromises = line.stations.map(station => addStation(station));

            Promise.all(ensurePromises)
                .then(() => {
                    const stopPromises = line.stations.map((station, i) => {
                        return new Promise((resolveStop, rejectStop) => {
                            db.run(stopsSQL, [lineID, station.id, i], function(err) {
                                if (err) {
                                    console.error(err);
                                    return rejectStop(err);
                                }
                                resolveStop();
                            })
                        })
                    })
                    const connectionPromises = line.stations.slice(0, line.stations.length-1).map((station, i) => {
                        return new Promise((resolveConnection, rejectConnection) => {
                            const nextStation = line.stations[i+1];
                            db.run(connectionSQL, [station.id, nextStation.id, lineID], function(err) {
                                if (err) {
                                    console.error(err);
                                    return rejectConnection(err);
                                }
                                resolveConnection();
                            })
                        })
                    })

                    return Promise.all(stopPromises)
                        .then(() => Promise.all(connectionPromises))
                        .then(() => resolve(lineID));
                })
                .catch(err => reject(err));
        });
    })
}

function getLineStops(lineID) {
    return new Promise((resolve, reject) => {
        const sql = `
            SELECT stations.id, stations.name
            FROM line_stations
            JOIN stations ON line_stations.stationID = stations.id
            WHERE line_stations.lineID = ?
            ORDER BY line_stations.stopOrder
        `;
        db.all(sql, [lineID], (err, rows) => {
            if (err) {
                console.error(err);
                reject(err);
            }
            else {
                const stops = rows.map(row => new Station({id: row.id, name: row.name}));
                resolve(stops);
            }
        })
    })
}

