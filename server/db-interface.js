//#region STATIONS

function getAllStations() {
    return new Promise((resolve, reject) => {
        const sql = 'SELECT * FROM stations';
        db.all(sql, (err, rows) => {
            if (err) {
                console.log(err);
                reject(err);
            }
            else {
                const stations = rows.map(row => new Station(row));
                resolve(stations);
            }
        })
    })
}

function getStation(stationID) {
    return new Promise((resolve, reject) => {
        const sql = 'SELECT * FROM stations WHERE id = ?';
        db.get(sql, [stationID], (err, row) => {
            if (err) {
                console.error(err);
                return reject(err);
            }
            resolve(new Station({id: stationID, name: row.name}));
        })
    })
}

/**
 * Checks if a station is already present in the database and, if not, adds it
 * to it.
 * @param {Station} station - Station object to add, wether new or old, with
 * optionally defined id. Name must be defined. 
 * @returns A promise that resolves in the station ID in the database. If the station
 * was already present in the table, the old ID is returned, otherwise a new one
 * with autoincrement is generated and returned.
 */
function addStation(station) {
    return new Promise((resolve, reject) => {
        const insertOrIgnoreSQL = 'INSERT OR IGNORE INTO stations(name) VALUES(?)';
        const selectSQL = 'SELECT id FROM stations WHERE name = ?';

        db.run(insertOrIgnoreSQL, [station.name], function(err) {
            if (err) return reject(err);
            db.get(selectSQL, [station.name], (err, row) => {
                if (err) return reject(err);
                if (!row) return reject(new Error(`Failed to get station id for ${station.name}`));
                station.id = row.id;
                resolve(row.id);
            });
        });
    });
}

